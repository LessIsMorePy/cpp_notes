#include <iostream>
using namespace std;

int main()
{
  int a, b, c;

  a = 10;
  b = 20;
  c = 30;

  cout << "A is greater than B: " << (a > b) << endl;
  cout << "A is less than B: " << (a < b) << endl;
  cout << "A is equal to B: " << (a == b) << endl;
  cout << "A is distinct to B: " << (a != b);

  return 0;
}
